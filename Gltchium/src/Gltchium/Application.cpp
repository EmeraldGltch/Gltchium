	void Application::Run() {
		WindowResizeEvent e(1280, 720);

		if (e.IsInCategory(EventCategoryApplication)) {
			GC_TRACE(e);
		}

		if (e.IsInCategory(EventCategoryInput)) {
			GC_TRACE(e);
		}

		while (true);
	}

